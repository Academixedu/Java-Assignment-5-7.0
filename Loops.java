public class Loops{
// Prepare a For Loop and While  in each Function
}
public class Loops {
    public static void main(String[] args) {
     int a,b;
     a=2;
     b=4;
     for (int i = 0; i < 10; i++) {
        System.out.println(a);
         
     }
     int counter=0;
     while (counter<b) {
        System.out.println(b);
        counter++; 
         
     }

    }
    
}
