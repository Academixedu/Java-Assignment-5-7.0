public class Person
{
  // Take 4 Variables and Initialize them
  // Create Getters and Setters
  // Create Constructors
  private int a;
  private int b;
  private String s;

  public Person(int a, int b, String s, String c)
   {
    this.a = a;
    this.b = b;
    this.s = s;
    this.c = c;
  }
     
  private String c;

  public int getA()
   {
    return a;
  }
  public void setA(int a)
   {
    this.a = a;
  }
  public int getB() {
    return b;
  }
  public void setB(int b)
   {
    this.b = b;
  }
  public String getS() 
  {
    return s;
  }
  public void setS(String s)
   {
    this.s = s;
  }
  public String getC() 
  {
    return c;
  }
  public void setC(String c)
   {
    this.c = c;
  }
  
}
