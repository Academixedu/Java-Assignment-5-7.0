public class Loops{
// Prepare a For Loop and While  in each Function
public void forloop(){
    for(int i=0;i<10;i++){
        System.out.println(i);
    }
    }  
    public void whileloop(){
        int count=0;
        while(count<5){
            System.out.println(count);
            count++;
        }
    }
    
    
}

