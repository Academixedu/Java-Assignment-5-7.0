public class Loops{
// Prepare a For Loop and While  in each Function
public static void For(int a){
    for(int i=0;i<=a;i++)
    {
        System.out.println(i);
    }
}
    public static void While(int a)
    {
        int count=a;
        while(count>=0)
        {
            System.out.println(count);
            count--;
        }
    }   
}
